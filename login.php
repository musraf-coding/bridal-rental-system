<?php
session_start(); // Start the session

// Database connection
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "bridal"; // Replace with your database name

$conn = new mysqli($servername, $username, $password, $dbname);

// Check connection
if ($conn->connect_error) {
    die("Database Connection Failed: " . $conn->connect_error);
}

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    if (isset($_POST['action'])) {
        if ($_POST['action'] === 'register') {
            // Securely handle registration
            $name = trim($_POST['name']);
            $email = trim($_POST['email']);
            $city = trim($_POST['city']);
            $password = password_hash($_POST['password'], PASSWORD_BCRYPT);

            // Check if email exists
            $stmt = $conn->prepare("SELECT id FROM users WHERE email = ?");
            $stmt->bind_param("s", $email);
            $stmt->execute();
            $stmt->store_result();

            if ($stmt->num_rows > 0) {
                echo "<script>
                    alert('Email already registered.');
                    window.location.href = 'login.php'; 
                </script>";
            } else {
                // Insert new user
                $stmt = $conn->prepare("INSERT INTO users (name, email, city, password) VALUES (?, ?, ?, ?)");
                $stmt->bind_param("ssss", $name, $email, $city, $password);

                if ($stmt->execute()) {
                    echo "<script>
                        alert('Registration successful.');
                        window.location.href = 'login.php'; 
                    </script>";
                } else {
                    echo "Error: " . $stmt->error;
                }
            }
            $stmt->close();
        } elseif ($_POST['action'] === 'login') {
            // Securely handle login
            $email = trim($_POST['email']);
            $password = $_POST['password'];

            // ðŸ”¹ **Directly check if the email is "admin@gmail.com"**
            if ($email === "admin@gmail.com") {
                $_SESSION['user_id'] = "admin";
                $_SESSION['user_name'] = "Admin";
                $_SESSION['loggedin'] = true;

                echo "<script>
                    window.location.href = 'dashboard.php'; 
                </script>";
                exit(); // Stop further execution
            }

            // If not admin, proceed with normal user login
            $stmt = $conn->prepare("SELECT id, name, password FROM users WHERE email = ?");
            $stmt->bind_param("s", $email);
            $stmt->execute();
            $result = $stmt->get_result();

            if ($result->num_rows > 0) {
                $user = $result->fetch_assoc();

                if (password_verify($password, $user['password'])) {
                    $_SESSION['user_id'] = $user['id'];
                    $_SESSION['user_name'] = $user['name'];
                    $_SESSION['loggedin'] = true;

                    echo "<script>
                        window.location.href = 'index.php'; 
                    </script>";
                } else {
                    echo "<script>
                        alert('Invalid password.');
                        window.location.href = 'login.php'; 
                    </script>";
                }
            } else {
                echo "<script>
                    alert('Email not found!');
                    window.location.href = 'login.php'; 
                </script>";
            }
            $stmt->close();
        }
    }
}

$conn->close();
